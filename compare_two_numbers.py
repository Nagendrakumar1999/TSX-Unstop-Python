num_1 = int(input("Enter the first number: "))
num_2 = int(input("Enter the second number: "))
#comparison of two numbers
if num_1 > num_2:
    print(num_1, "is greater than", num_2)
elif num_1 < num_2:
    print(num_2, "is greater than", num_1)
else:
    print(num_1, "and", num_2, "are equal")